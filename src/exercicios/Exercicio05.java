package exercicios;

/**
 * Programa para exibir idade, altura e peso atribuidos em variáveis
 * 
 * @author Diego Munhoz
 * @since 05/02/2021
 */
public class Exercicio05 {

	/*
	 * Método principal para executar o programa
	 */
	public static void main(String[] args) {

		// Declarando as variáveis
		int idade;
		double altura, peso;

		// atribuindo valores para as variaveis
		idade = 37;
		altura = 1.75;
		peso = 80.5;

		// exibindo os dados no console
		System.out.println("Dados da variável IDADE: " + idade);
		System.out.println("Dados da variável ALTURA: " + altura);
		System.out.println("Dados da variável PESO: " + peso);

	}

}
