package exercicios;

/**
 * Programa para exibir nome, celular e cidade atribuidos em variáveis
 * 
 * @author Diego Munhoz
 * @since 05/02/2021
 */
public class Exercicio04 {

	/*
	 * Método principal para executar o programa
	 */
	public static void main(String[] args) {

		// Declarando as variáveis
		String nome, celular, cidade;

		// atribuindo valores para as variaveis
		nome = "Diego Munhoz";
		celular = "(11) 98116-4359";
		cidade = "Barueri";

		// exibindo os dados no console
		System.out.println("Dados da variável NOME: " + nome);
		System.out.println("Dados da variável CELULAR: " + celular);
		System.out.println("Dados da variável CIDADE: " + cidade);

	}

}
